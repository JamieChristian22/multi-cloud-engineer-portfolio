CREATE TABLE customers (customer_id TEXT PRIMARY KEY, first_name TEXT, last_name TEXT, email TEXT UNIQUE, city TEXT, state TEXT);
CREATE TABLE products (sku TEXT PRIMARY KEY, product_name TEXT, unit_price NUMERIC(10,2), category TEXT);
CREATE TABLE orders (order_id TEXT PRIMARY KEY, customer_id TEXT REFERENCES customers(customer_id), order_date DATE);
CREATE TABLE order_items (order_id TEXT REFERENCES orders(order_id), sku TEXT REFERENCES products(sku), quantity INT, unit_price NUMERIC(10,2));
CREATE INDEX idx_orders_date ON orders(order_date);
CREATE INDEX idx_items_order ON order_items(order_id);
